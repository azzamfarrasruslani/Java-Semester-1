public class Pertemuan3Lat3 {
    public static void main(String[] args) {
        // Menampilkan bilangan kelipatan 3 menggunakan for
        System.out.println("Bilangan kelipatan 3 (0 - 50) menggunakan for:");
        for (int i = 0; i <= 50; i += 3) {
            System.out.print(i + " ");
        }
    }
}
