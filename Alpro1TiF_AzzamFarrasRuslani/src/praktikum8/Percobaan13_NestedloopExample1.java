public class Percobaan13_NestedloopExample1 {

    public static void main(String[] args) {
        int i, j;

        for (i = 1; i <= 3; i++) {
            for (j = 1; j <= 3; j++) {
                System.out.println(j);
            }
            System.out.println("");
        }
    }
}
