public class Percobaan2_UsingVariabel {

    public static void main(String[] args) {
        byte myByte = 10;
        int myNum = 50000;
        float myFloatNum = 5.99f;
        double myDoubleNum = 200.9d;
        char myLetter = 'D';
        boolean myBool = true;
        String myText = "PCR";

        System.out.println(myByte + "ini adalah bilangan bertipe data byte");
        System.out.println(myNum + "ini adalah bilangan bertipe data integer");
        System.out.println(myFloatNum + "ini adalah bilangan bertipe data float");
        System.out.println(myDoubleNum + "ini adalah bilangan bertipe data double");
        System.out.println(myLetter + "adalah sebuah karakter");
        System.out.println(myBool + "adalah nilai boolean");
        System.out.println("I AM" + myText);

    }
}
