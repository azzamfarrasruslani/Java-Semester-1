public class Percobaan3_AssignmentOperator {

    public static void main(String[] args) {
        // create variable
        int a = 4;
        int var;

        // assign value using =
        var = a;
        System.out.println("var using =: " + var);

        // assing value using =+
        var += a;
        System.out.println("var using +=: " + var);

        // assign value using =*
        var *= a;
        System.out.println("ver using *=: " + var);
    }
}
